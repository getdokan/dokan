            dokan.api.get( '/admin/modules' )
                .done( response => {
                    this.modules = response;
                    this.isLoaded = true;
                } );
        },

        onSwitch( status, moduleSlug ) {
            let moduleData = _.findWhere( this.modules, { id: moduleSlug } );

            if ( status ) {
                // Need to activate
                let message = moduleData.name + this.__( 'is successfully activated', 'dokan' );

                dokan.api.put( '/admin/modules/activate', {
                    module: [ moduleSlug ]
                } )
                    .done( response => {
                        this.$notify({
                            title: 'Success!',
                            type: 'success',
                            text: message,
                        });

                        this.toggleActivation = false;
                        location.reload();
                    } );

            } else {
                // Need to deactivate
                let message = moduleData.name + this.__( 'is successfully deactivated', 'dokan' );

                dokan.api.put( '/admin/modules/deactivate', {
                    module: [ moduleSlug ]
                } )
                    .done(response => {
                        this.$notify({
                            title: 'Success!',
                            type: 'success',
                            text: message,
                        });

                        location.reload();
                    });
            }
        },

        deselectCategory( index ) {
            this.selected_categories.splice( index, 1 );
        },

        toggleSelect() {
            if( this.isAllSelected ) {
                return this.selected_modules = [];
            }

            return this.selected_modules = this.filteredModules.map( module => module.id );
        },

        onBulkAction( action ) {
            let message;

            if ( 'activate' === action ) {
                this.is_activating = true;
                message = this.__( 'All selected modules are successfully activated', 'dokan' );
            } else {
                this.is_deactivating = true;
                message = this.__('All selected modules are successfully deactivated', 'dokan');
            }

            dokan.api.put('/admin/modules/' + action, {
                module: this.selected_modules
            })
                .done(response => {
                    this.is_activating = false;
                    this.is_deactivating = false;
                    this.selected_modules = [];
                    this.fetchModules();
                    this.$notify({
                        title: 'Success!',
                        type: 'success',
                        text: message,
                    });
                });
        },

        toggleSidebar() {
            return this.isOpenSidebar = ! this.isOpenSidebar;
        }
    },

    created() {
        if ( typeof(localStorage) != 'undefined' ) {
            this.currentView = localStorage.getItem("active_view") ? localStorage.getItem("active_view") : 'grid-view';
        }

        this.fetchModules();
    },

}
